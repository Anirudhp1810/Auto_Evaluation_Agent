import os
from fpdf import FPDF
from datetime import datetime

class ReportPDF(FPDF):
    def header(self):
        self.set_font("helvetica", "B", 15)
        # Adding a title to header
        self.cell(0, 10, "EvalAI - Automated Evaluation Report", border=False, align="C")
        self.ln(20)

    def footer(self):
        self.set_y(-15)
        self.set_font("helvetica", "I", 8)
        self.cell(0, 10, f"Page {self.page_no()} | Generated by EvalAI on {datetime.now().strftime('%Y-%m-%d %H:%M')}", 0, 0, "C")

def generate_student_report(student_name, student_id, roll_no, exam_name, marks_data, total_marks, max_marks, grade, overall_feedback, output_dir="results/reports"):
    os.makedirs(output_dir, exist_ok=True)
    
    pdf = ReportPDF()
    pdf.add_page()
    
    # Student Info Section
    pdf.set_font("helvetica", "B", 12)
    pdf.cell(0, 10, "Student Details", ln=1)
    
    pdf.set_font("helvetica", "", 10)
    pdf.cell(50, 8, f"Name: {student_name}", ln=0)
    pdf.cell(50, 8, f"ID: {student_id}", ln=0)
    pdf.cell(50, 8, f"Roll No: {roll_no}", ln=1)
    pdf.cell(50, 8, f"Exam: {exam_name}", ln=1)
    pdf.ln(5)
    
    # overall score
    pdf.set_font("helvetica", "B", 12)
    pdf.cell(50, 10, f"Total Score: {total_marks} / {max_marks}")
    pdf.cell(50, 10, f"Grade: {grade}", ln=1)
    pdf.ln(5)
    
    # Question Marks Table
    pdf.set_font("helvetica", "B", 11)
    pdf.cell(0, 10, "Question-wise Breakdown", ln=1)
    
    # Render Table using FPDF2 built-in table layout for perfect cell heights
    with pdf.table(col_widths=(30, 30, 130), first_row_as_headings=True) as table:
        # Table Header
        pdf.set_font("helvetica", "B", 10)
        header = table.row()
        header.cell("Question")
        header.cell("Marks")
        header.cell("Feedback")
        
        # Table Rows
        pdf.set_font("helvetica", "", 10)
        for q in marks_data:
            row = table.row()
            row.cell(str(q.get('question_id', 'N/A')))
            row.cell(f"{q.get('awarded', 0)} / {q.get('max', 0)}")
            row.cell(str(q.get('feedback', '')))

    pdf.ln(10)
    
    # AI Feedback Paragraph
    pdf.set_font("helvetica", "B", 12)
    pdf.cell(0, 10, "Overall AI Feedback Summary", ln=1)
    pdf.set_font("helvetica", "", 10)
    pdf.multi_cell(0, 8, overall_feedback)
    
    filename = f"{student_id}_{exam_name.replace(' ', '_')}_Report.pdf"
    filepath = os.path.join(output_dir, filename)
    pdf.output(filepath)
    
    return filepath

